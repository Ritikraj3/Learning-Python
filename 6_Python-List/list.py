# The list is changeable, meaning that 
# we can change, add, and remove items in a list 
# after it has been created.
myList = ["apple", "banana", "cherry"]

print(myList[0])
myList[0] = "orange"
print(myList[0])
print(myList[0:])
print(myList[0:3])
print(myList[0:len(myList)])

thislist = list(("book", "pen", "notebook", "pencil"))
print(thislist)
